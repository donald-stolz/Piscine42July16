void	rotate(char *str, int i)
{
	int c;

	c = 0;
	if (str[i] >= 65 && str[i] <= 73 && c != 1)
	{
		str[i] = str[i] + 16;
		c = 1;
	}
	if (str[i] >= 74 && str[i] <= 90 && c != 1)
	{
		str[i] = str[i] - 10;
		c = 1;
	}
	if (str[i] >= 97 && str[i] <= 105 && c != 1)
	{
		str[i] = str[i] + 16;
		c = 1;
	}
	if (str[i] >= 106 && str[i] <= 122 && c != 1)
	{
		str[i] = str[i] - 10;
	}
}

char	*ft_rot42(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		rotate(str, i);
		i++;
	}
	return (str);
}
