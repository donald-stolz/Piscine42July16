int	ft_button(int i, int j, int k)
{
	if (i > j && i < k)
	{
		return (i);
	}
	if (i < j && i > k)
	{
		return (i);
	}
	if (j > i && j < k)
	{
		return (j);
	}
	if (j < i && j > k)
	{
		return (j);
	}
	return (k);
}
