int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;
	int r;

	r = 0;
	while ((s1[i] || s2[i]) && r == 0 && i < n)
	{
		if (s1[i] != s2[i])
		{
			r = s1[i] - s2[i];
		}
		i++;
	}
	return (r);
}
