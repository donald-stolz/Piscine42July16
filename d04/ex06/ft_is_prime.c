int	ft_is_prime(int nb)
{
	int i;
	int count;

	i = 1;
	if (nb < 2)
	{
		return (0);
	}
	while (i < nb / 2)
	{
		i++;
		if (nb % i == 0)
		{
			count++;
		}
	}
	if (count > 1)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
